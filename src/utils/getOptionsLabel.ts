const getOptionsLabel = (index : number) => {
    switch (index) {
        case 0:
        return "A";
            break;
        case 1:
            return "B";
            
            break;
        case 2:
            return "C";
            
            break;
        case 3:
            return "D";
            break;
        default:
            break;
    }
}



export default getOptionsLabel